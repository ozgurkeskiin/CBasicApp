void appDebugPrintf(unsigned char *args, ...)
{
	char tmpStr[MAX_BUFFER_SIZE] = { 0 };
	va_list vaList;

	__crt_va_start(vaList, args);
	vsprintf_s(tmpStr, sizeof(tmpStr), args, vaList);
	__crt_va_end(vaList);

	printf("%s\n", tmpStr);
}